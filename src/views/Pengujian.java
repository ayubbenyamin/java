    private void loadData() {
        Object row[] = {"Kode Pengujian", "Nomor Pengujian", "Jenis Pengujian", "Metode Pengujian", "Sumber Pengujian", "Tanggal Jatuh Tempo"};
        DefaultTableModel tableModel = new DefaultTableModel(null, row) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

        };
        jTable1.setModel(tableModel);

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");

        for (model.Pengujian field : control.findPengujianEntities()) {
            String[] data = {
                field.getKodePengujian(),
                field.getNoPengujian(),
                field.getJenisPengujian(),
                field.getMetode(),
                field.getSumberPengujian(),
                sdf.format(field.getTglJatuhTempoPgjn())
            };
            tableModel.addRow(data);
        }
        Global.setEnabledTextField(jPanel1, false);
        Global.setClearTextField(jPanel1);
    }

    private void setModelData() {
        model = new model.Pengujian();
        model.setKodePengujian(jTextField1.getText());
        model.setNoPengujian(jTextField2.getText());
        model.setJenisPengujian(jTextField3.getText());
        model.setMetode(jTextField4.getText());
        model.setSumberPengujian(jTextArea1.getText());
        model.setTglJatuhTempoPgjn(jDateChooser1.getDate());
        model.setIdAdmin(Global.getModelAdmin());
    }

    private boolean validateField() {
        boolean error;
        Global.validatorComponent.clear();
        Global.validatorComponent.add(jTextField1);
        Global.validatorComponent.add(jTextField2);
        Global.validatorComponent.add(jTextField3);
        Global.validatorComponent.add(jTextField4);
        Global.validatorComponent.add(jTextArea1);
        Global.validatorComponent.add(jDateChooser1);

        if (error = Global.validatorComponentEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Silahkan isi semua bagian yang kosong", "Kosong", JOptionPane.WARNING_MESSAGE);
        }
        return error;
    }

