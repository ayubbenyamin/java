    private void loadData() {
        Object row[] = {"Kode Perizinan", "Nomor Perizinan", "Jenis Perizinan", "Kegunaan Perizinan", "Sumber Perizinan", "Tanggal Jatuh Tempo"};
        DefaultTableModel tableModel = new DefaultTableModel(null, row);
        jTable1.setModel(tableModel);

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");

        for (model.Perizinan field : control.findPerizinanEntities()) {
            String[] data = {
                field.getKodePerizinan(),
                field.getNoPerizinan(),
                field.getJenisPerizinan(),
                field.getKegunaanPerizinan(),
                field.getSumberPerizinan(),
                sdf.format(field.getTglJatuhTempoPrzn())
            };
            tableModel.addRow(data);
        }
        Global.setEnabledTextField(jPanel1, false);
        Global.setClearTextField(jPanel1);
    }

    private void setModelData() {
        model = new model.Perizinan();
        model.setKodePerizinan(jTextField1.getText());
        model.setNoPerizinan(jTextField2.getText());
        model.setJenisPerizinan(jTextField3.getText());
        model.setKegunaanPerizinan(jTextField4.getText());
        model.setSumberPerizinan(jTextField5.getText());
        model.setTglJatuhTempoPrzn(jDateChooser1.getDate());
        model.setIdAdmin(Global.getModelAdmin());
    }

    private boolean validateField() {
        boolean error;
        Global.validatorComponent.clear();
        Global.validatorComponent.add(jTextField1);
        Global.validatorComponent.add(jTextField2);
        Global.validatorComponent.add(jTextField3);
        Global.validatorComponent.add(jTextField4);
        Global.validatorComponent.add(jTextField5);
        Global.validatorComponent.add(jDateChooser1);

        if (error = Global.validatorComponentEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Silahkan isi semua bagian yang kosong", "Kosong", JOptionPane.WARNING_MESSAGE);
        }
        return error;
    }

