    private void loadData() {
        Object row[] = {"Kode Sertifikasi", "Nomor Sertifikasi", "Jenis Sertifikasi", "Sumber Sertifikasi", "Tanggal Jatuh Tempo"};
        DefaultTableModel tableModel = new DefaultTableModel(null, row) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

        };
        jTable1.setModel(tableModel);

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");

        for (model.Sertifikasi field : control.findSertifikasiEntities()) {
            String[] data = {
                field.getKodeSertifikasi(),
                field.getNoSertifikasi(),
                field.getJenisSertifikasi(),
                field.getSumberSertifikasi(),
                sdf.format(field.getTglJatuhTempoSrks())
            };
            tableModel.addRow(data);
        }
        Global.setEnabledTextField(jPanel1, false);
        Global.setClearTextField(jPanel1);
    }

    private void setModelData() {
        model = new model.Sertifikasi();
        model.setKodeSertifikasi(jTextField1.getText());
        model.setNoSertifikasi(jTextField2.getText());
        model.setJenisSertifikasi(jTextField3.getText());
        model.setSumberSertifikasi(jTextArea1.getText());
        model.setTglJatuhTempoSrks(jDateChooser1.getDate());
        model.setIdAdmin(Global.getModelAdmin());
    }

    private boolean validateField() {
        boolean error;
        Global.validatorComponent.clear();
        Global.validatorComponent.add(jTextField1);
        Global.validatorComponent.add(jTextField2);
        Global.validatorComponent.add(jTextField3);
        Global.validatorComponent.add(jTextArea1);
        Global.validatorComponent.add(jDateChooser1);

        if (error = Global.validatorComponentEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Silahkan isi semua bagian yang kosong", "Kosong", JOptionPane.WARNING_MESSAGE);
        }
        return error;
    }

